    public static final int SYSTEM_WIN_DIR4 = -102;

    private final int[] colors;
    private final String name;
    private final java.util.Map<Integer, Integer> nearestCache = new java.util.HashMap<>();

    public Palette(int[] colors, String name) {
        this.colors = colors;
        this.name = name;
    }

    public int getColor(int index) {
        if (index >= 0 && index < colors.length) {
            return colors[index];
        }
        return 0;
    }

    public int[] getRGB(int index) {
        int color = getColor(index);
        return new int[] {
            (color >> 16) & 0xFF,
            (color >> 8) & 0xFF,
            color & 0xFF
        };
    }

    public int size() {
        return colors.length;
    }

    public int nearestIndex(int rgb) {
        int target = rgb & 0xFFFFFF;
        Integer cached = nearestCache.get(target);
        if (cached != null) {
            return cached;
        }
        int tr = (target >> 16) & 0xFF;
        int tg = (target >> 8) & 0xFF;
        int tb = target & 0xFF;
        int bestIndex = 0;
        int bestDistance = Integer.MAX_VALUE;
        for (int i = 0; i < colors.length; i++) {
            int color = colors[i] & 0xFFFFFF;
            int dr = tr - ((color >> 16) & 0xFF);
            int dg = tg - ((color >> 8) & 0xFF);
            int db = tb - (color & 0xFF);
            int distance = (dr * dr) + (dg * dg) + (db * db);
            if (distance < bestDistance) {
                bestDistance = distance;
                bestIndex = i;
                if (distance == 0) {
                    break;
                }
            }
        }
        nearestCache.put(target, bestIndex);
        return bestIndex;
    }

    public String getName() {
        return name;
    }

    public static String getBuiltInSymbolName(int paletteId) {
        return switch (paletteId) {
            case SYSTEM_MAC -> "systemMac";
            case RAINBOW -> "rainbow";
            case GRAYSCALE -> "grayscale";
            case PASTELS -> "pastels";
            case VIVID -> "vivid";
            case NTSC -> "ntsc";
            case METALLIC -> "metallic";
            case SYSTEM_WIN, SYSTEM_WIN_DIR4 -> "systemWin";
            default -> null;
        };
    }

    public static Palette getBuiltInBySymbolName(String symbolName) {
        if (symbolName == null) {
            return null;
        }
        return switch (symbolName.trim().toLowerCase(java.util.Locale.ROOT)) {
            case "systemmac" -> SYSTEM_MAC_PALETTE;
            case "rainbow" -> RAINBOW_PALETTE;
            case "grayscale", "greyscale" -> GRAYSCALE_PALETTE;
            case "metallic" -> METALLIC_PALETTE;
            case "systemwin", "systemwindows" -> SYSTEM_WIN_PALETTE;
            default -> null;
        };
    }

    public static String normalizeBuiltInSymbolName(String symbolName) {
        if (symbolName == null) {
            return null;
        }
        return switch (symbolName.trim().toLowerCase(java.util.Locale.ROOT)) {
            case "systemmac" -> "systemMac";
            case "rainbow" -> "rainbow";
            case "grayscale", "greyscale" -> "grayscale";
            case "pastels" -> "pastels";
            case "vivid" -> "vivid";
            case "ntsc" -> "ntsc";
            case "metallic" -> "metallic";
            case "systemwin", "systemwindows" -> "systemWin";
            default -> null;
        };
    }

    /**
     * Get a built-in palette by ID.
     */
    public static Palette getBuiltIn(int paletteId) {
        return switch (paletteId) {
            case SYSTEM_MAC -> SYSTEM_MAC_PALETTE;
            case RAINBOW -> RAINBOW_PALETTE;
            case GRAYSCALE -> GRAYSCALE_PALETTE;
            case METALLIC -> METALLIC_PALETTE;
            case SYSTEM_WIN, SYSTEM_WIN_DIR4 -> SYSTEM_WIN_PALETTE;
            default -> SYSTEM_MAC_PALETTE; // Default
        };
    }

