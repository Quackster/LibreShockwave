std::vector<std::uint32_t> grayscaleColors() {
    std::vector<std::uint32_t> grays;
    grays.reserve(256);
    for (int index = 0; index < 256; ++index) {
        const auto value = static_cast<std::uint32_t>(255 - index);
        grays.push_back((value << 16) | (value << 8) | value);
    }
    return grays;
}

