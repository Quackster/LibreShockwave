Palette::Palette(std::vector<std::uint32_t> colors, std::string name)
    : colors_(std::move(colors)), name_(std::move(name)) {}

std::uint32_t Palette::getColor(int index) const {
    if (index >= 0 && index < static_cast<int>(colors_.size())) {
        return colors_[static_cast<std::size_t>(index)];
    }
    return 0;
}

std::array<int, 3> Palette::getRGB(int index) const {
    const auto color = getColor(index);
    return {
        static_cast<int>((color >> 16) & 0xFF),
        static_cast<int>((color >> 8) & 0xFF),
        static_cast<int>(color & 0xFF)
    };
}

int Palette::size() const {
    return static_cast<int>(colors_.size());
}

int Palette::nearestIndex(std::uint32_t rgb) const {
    const auto target = rgb & 0xFFFFFFU;
    if (const auto cached = nearestCache_.find(target); cached != nearestCache_.end()) {
        return cached->second;
    }

    const int tr = static_cast<int>((target >> 16) & 0xFF);
    const int tg = static_cast<int>((target >> 8) & 0xFF);
    const int tb = static_cast<int>(target & 0xFF);
    int bestIndex = 0;
    int bestDistance = std::numeric_limits<int>::max();

    for (int index = 0; index < static_cast<int>(colors_.size()); ++index) {
        const auto color = colors_[static_cast<std::size_t>(index)] & 0xFFFFFFU;
        const int dr = tr - static_cast<int>((color >> 16) & 0xFF);
        const int dg = tg - static_cast<int>((color >> 8) & 0xFF);
        const int db = tb - static_cast<int>(color & 0xFF);
        const int distance = (dr * dr) + (dg * dg) + (db * db);
        if (distance < bestDistance) {
            bestDistance = distance;
            bestIndex = index;
            if (distance == 0) {
                break;
            }
        }
    }

    nearestCache_[target] = bestIndex;
    return bestIndex;
}

const std::string& Palette::name() const {
    return name_;
}

