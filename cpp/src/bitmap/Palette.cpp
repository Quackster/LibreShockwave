const Palette* Palette::builtInBySymbolName(std::string_view symbolName) {
    const auto key = normalize(symbolName);
    if (key == "systemmac") return &systemMacPalette();
    if (key == "rainbow") return &rainbowPalette();
    if (key == "grayscale" || key == "greyscale") return &grayscalePalette();
    if (key == "metallic") return &metallicPalette();
    if (key == "systemwin" || key == "systemwindows") return &systemWinPalette();
    return nullptr;
}

